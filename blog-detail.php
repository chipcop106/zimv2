              <article>
                <blockquote class="messageText">
                      Thương hiệu <a href="tags/ifi/" class="Tinhte_XenTag_TagLink" title="">iFi</a> đã chính thức giới thiệu dòng sản phẩm <a href="tags/zen/" class="Tinhte_XenTag_TagLink" title="">Zen</a> Series bao gồm các mẫu thiết bị âm thanh Desktop giá rẻ. <a href="tags/zen-blue/" class="Tinhte_XenTag_TagLink" title="">Zen Blue</a> là mẫu sản phẩm đầu tiên trong dòng sản phẩm Zen được giới thiệu là một chiếc Balanced Bluetooth receiver có mức giá vừa phải chỉ 129 Bảng Anh (khoảng 3,7tr VNĐ).<br>
                      <br>
                        <span class="bdImage_attachImage">
                        <span class="inner">
                          <img src="https://photo2.tinhte.vn/data/attachment-files/2019/10/4795696_iFi_Zen_Blue_p2.jpg" alt="Đang tải iFi_Zen_Blue_p2.jpg…" title="iFi_Zen_Blue_p2.jpg" data-width="1800" data-height="1350">
                        </span>
                      </span>
                      <br>
                      <br>
                      Mẫu Zen Blue có thể kết nối với đa số các thiết bị âm thanh hiện nay với nhiều đầu output khác nhau bao gồm RCA 2V (unbalanced), 4.4mm Pentaconn balanced 4V có thể chuyển đổi với các cổng XLR balanced thông thường, đầu coaxial hoặc optical output để xuất tín hiệu digital sang các DAC khác.<br>
                      <br>
                      Sản phẩm Zen Blue sẽ hỗ trợ đa số các codec cao cấp hiện nay như: Qualcomm aptX HD (24-bit/48kHz), aptX Low Latency, các codec như Sony LDAC (24-bit/96kHz) và Huawei HWA/LHDC (24-bit/96kHz) và AAC codec cho các sản phẩm của Apple.<br>
                      <br>
                        <span class="bdImage_attachImage">
                        <span class="inner">
                          <img src="https://photo2.tinhte.vn/data/attachment-files/2019/10/4795698_iFi_Zen_Blue_p4.jpg" alt="Đang tải iFi_Zen_Blue_p4.jpg…" title="iFi_Zen_Blue_p4.jpg" data-width="1800" data-height="1115">
                        </span>
                      </span>
                      <br>
                      <br>
                      Mặt trước của Zen Blue có hai đèn LED thay đổi màu sắc để thể hiện tình trạng hoạt động kết nối của máy. Một đèn báo lớn có logo iFi hiển thị codec đang được kết nối và một đèn bên cạnh thể hiện sample-rate đang được giải mã.<br>
                      <br>
                      Mẫu Zen Blue là sản phẩm Bluetooth Receiver đầu tiên sử dụng chip receiver, bộ xử lý kiêm DAC chip xử lý QCC5100. Tuy nhiên iFi cũng không đơn giản sử dụng giải pháp xử lý mặc định của chip Soc, hãng cũng sử dụng nhiều tùy chỉnh để cải thiện chất lượng âm thanh.<br>
                      Sản phẩm sẽ tách riêng phần mạch xử lý digital và xuất âm analog riêng biệt. Đặc biệt tín hiệu âm thanh không những chỉ được xử lý thông qua QCC5100 chipset mà sau đó còn được đưa qua một chip DAC ESS Sabre để sử dụng bộ xử lý sửa lỗi thời gian chính xác hơn và gia tăng độ động, chi tiết của âm thanh hơn so với DAC tích hợp.<br>
                      <br>
                        <span class="bdImage_attachImage">
                        <span class="inner" >
                          <img src="https://photo2.tinhte.vn/data/attachment-files/2019/10/4795695_iFi_Zen_Blue_p3.jpg" alt="Đang tải iFi_Zen_Blue_p3.jpg…" title="iFi_Zen_Blue_p3.jpg" data-width="1800" data-height="1350">
                        </span>
                      </span>
                      <br>
                      <br>
                      Phần mạch output stage của Zen Blue cũng khá đặc biệt ở phân khúc giá này khi được thiết kế hoàn toàn balanced có thể sử dụng với các sản phẩm balanced output. Mạch xuất âm sử dụng thiết kế Op-Amp với dòng OV series mới để đảm bảo độ tuyến tính của âm thanh.<br>
                      Phần cấp nguồn sử dụng một mạch lọc xử lý của Texas Instruments để làm giảm được độ nhiễu noise, nền âm sạch hơn.<br>
                      <br>
                      Sản phẩm tiếp theo trong dòng sản phẩm Zen Series sẽ là mẫu Zen DAC đổi phần input Bluetooth của Zen Blue thành đầu USB truyền thống và thêm một mạch headphone amp tích hợp.<br>
                      <br>
                </blockquote>
